package lista1ex4;

public class Gabarito {
    
    public char respostaQuestao(int numQuestao){
        
        if(numQuestao == 1) return 'C';
        else if(numQuestao == 2) return 'A';
        else if(numQuestao == 3) return 'B';
        else if(numQuestao == 4) return 'E';
        else if(numQuestao == 5) return 'D';
        else if(numQuestao == 6) return 'C';
        else if(numQuestao == 7) return 'E';
        else if(numQuestao == 8) return 'D';
        else if(numQuestao == 9) return 'B';
        else if(numQuestao == 10) return 'C';
        else if(numQuestao == 11) return 'A';
        else if(numQuestao == 12) return 'D';
        else if(numQuestao == 13) return 'E';
        else if(numQuestao == 14) return 'B';
        else return 'A';
    }
    
}
